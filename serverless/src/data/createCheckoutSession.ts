import { SSMClient, GetParameterCommand } from "@aws-sdk/client-ssm"
import Stripe from "stripe"

const ssmClient = new SSMClient({ region: process.env.AWS_REGION })

export const createCheckoutSession = async (
    params: Pick<
        Stripe.Checkout.SessionCreateParams,
        "line_items" | "cancel_url" | "success_url"
    >
) => {
    const getParameterCommand = new GetParameterCommand({
        Name: "STRIPE_SECRET_KEY",
    })

    const { Parameter } = await ssmClient.send(getParameterCommand)
    const secretValue = Parameter?.Value as string

    const stripe = new Stripe(secretValue, {
        apiVersion: "2023-10-16",
    })

    const { line_items, cancel_url, success_url } = params

    try {
        const session = await stripe.checkout.sessions.create({
            ui_mode: "embedded",
            mode: "payment",
            return_url: `${success_url}/success?session_id={CHECKOUT_SESSION_ID}`,
            line_items,
            allow_promotion_codes: true,
            shipping_address_collection: { allowed_countries: ["GB"] },
        })

        return {
            session,
        }
    } catch (error) {
        console.log("Error creating session")
        console.error(error)
        const errorMessage = "Could not create session"

        return {
            error: errorMessage,
        }
    }
}

